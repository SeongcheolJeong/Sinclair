def build_asset_metadata(entry: dict) -> dict:
    mesh_rel = f"{entry['category']}/{entry['id']}.obj"
    meta_rel = f"{entry['category']}/{entry['id']}.json"
    radarsim_target = {
        "model": mesh_rel,
        "location": [0.0, 0.0, 0.0],
        "rotation": [0.0, 0.0, 0.0],
        "scale": 1.0,
    }
    if entry["permittivity"] is not None:
        radarsim_target["permittivity"] = entry["permittivity"]

    return {
        "id": entry["id"],
        "class": entry["class"],
        "source": "sinclair_synthetic_canonical_mesh",
        "license": "project-generated-no-external-asset-dependency",
        "fidelity": "canonical_mesh_estimate",
        "mesh": mesh_rel,
        "metadata": meta_rel,
        "units": "meters",
        "dimensionsM": entry["dimensionsM"],
        "materialPreset": entry["materialPreset"],
        "nominalRcsDbsm": entry["nominalRcsDbsm"],
        "permittivity": entry["permittivity"],
        "defaultAspectSweepDeg": entry["defaultAspectSweepDeg"],
        "rcsUsagePolicy": {
            "rawSimRcsAbsoluteDbsm": "not_validated_for_physical_truth",
            "recommended": "Use RadarSim sim_rcs for aspect-angle response shape, then normalize to nominalRcsDbsm until measured or validated CAD/material data is available.",
            "calibrationDbsmAtBroadside": entry["nominalRcsDbsm"],
            "futureUpgrade": "Replace nominalRcsDbsm with measured RCS tables or validated ray-traced RCS distributions.",
        },
        "radarsim": {
            "engine": "radarsimpy_whitebox.sim_rcs",
            "targetTemplate": radarsim_target,
            "recommendedFrequencyHz": 77e9,
        },
        "limitations": [
            "Not measured RCS data.",
            "Not high-fidelity automotive CAD.",
            "Raw sim_rcs absolute magnitude is not validated as physical truth for these canonical meshes.",
            "No radome, ground multipath, pose articulation, or material-layer model.",
            "Use for fast design estimates before measured or ray-traced assets are connected.",
        ],
        "notes": entry["notes"],
    }


def main() -> None:
    ASSET_ROOT.mkdir(parents=True, exist_ok=True)
    manifest_assets = []

    for entry in ASSETS:
        metadata = build_asset_metadata(entry)
        mesh = entry["mesh"]()
        mesh_path = ASSET_ROOT / metadata["mesh"]
        meta_path = ASSET_ROOT / metadata["metadata"]
        write_obj(mesh_path, mesh, entry["id"])
        write_json(meta_path, metadata)
        manifest_assets.append({key: metadata[key] for key in ("id", "class", "fidelity", "mesh", "metadata", "dimensionsM", "materialPreset")})

    manifest = {
        "schema": "sinclair.radarRcsAssetLibrary.v1",
        "generatedBy": "scripts/generate_radar_rcs_assets.py",
        "root": "/assets/radar-rcs-library",
        "rcsEngine": "RadarSim radarsimpy_whitebox.sim_rcs",
        "defaultFrequencyHz": 77e9,
        "assetCount": len(manifest_assets),
        "assets": manifest_assets,
        "sourceBoundary": {
            "current": "Synthetic canonical mesh assets generated inside this project.",
            "notIncluded": "Measured vehicle/pedestrian/cyclist RCS data, production CAD, full EM ray tracing.",
            "recommendedNextStep": "Add measured RCS tables or validated CAD/material assets while keeping this manifest schema.",
        },
        "rcsUsagePolicy": {
            "recommended": "Do not use raw sim_rcs absolute dBsm from these simple canonical meshes as truth. Use it for angle-response shape and calibrate to each asset's nominalRcsDbsm.",
            "why": "Simple synthetic meshes and the current whitebox facet-sum path can produce unrealistic absolute magnitudes.",
        },
    }
    write_json(ASSET_ROOT / "manifest.json", manifest)

    readme = """# Sinclair Radar RCS Asset Library

This folder contains project-generated canonical mesh assets for early RadarSim
RCS experiments.

These files are not measured RCS data and not production CAD. They are simple
OBJ meshes with JSON metadata so Sinclair can move from hard-coded RCS numbers
to a visible asset-backed RCS source.

Important:

- Do not treat raw `sim_rcs` absolute dBsm from these meshes as measured truth.
- Use `sim_rcs` for aspect-angle response shape first.
- Normalize or calibrate that shape to each asset's `nominalRcsDbsm` until a
  measured RCS table or validated CAD/material model is available.

Recommended source modes:

1. Scenario Assumption
   - fast preset RCS values
   - no mesh calculation

2. RadarSim Mesh RCS
   - use these OBJ files with `radarsimpy_whitebox.sim_rcs`
   - good for early angle sweep and Pd estimate workflows

3. Measured RCS Library
   - future validated table/distribution source

4. High-Fidelity Ray Trace / EM Solver
   - future openEMS/Meep/WiTwin-style backend

Coordinate convention:

- x: object forward/length axis
- y: object lateral/width axis
- z: height axis
- units: meters

Asset boundary:

- Project-generated, synthetic, canonical geometry.
- No external mesh license dependency.
- Not measured or ray-traced truth.
"""
    (ASSET_ROOT / "README.md").write_text(readme, encoding="utf-8")

    print(f"Generated {len(manifest_assets)} radar RCS assets in {ASSET_ROOT}")


if __name__ == "__main__":
    main()
