Vec3 = tuple[float, float, float]
Face = tuple[int, ...]


@dataclass
class Mesh:
    vertices: list[Vec3]
    faces: list[Face]

    def extend(self, other: "Mesh") -> None:
        offset = len(self.vertices)
        self.vertices.extend(other.vertices)
        self.faces.extend(tuple(index + offset for index in face) for face in other.faces)


def cuboid(center: Vec3, size: Vec3) -> Mesh:
    cx, cy, cz = center
    sx, sy, sz = (value / 2 for value in size)
    vertices = [
        (cx - sx, cy - sy, cz - sz),
        (cx + sx, cy - sy, cz - sz),
        (cx + sx, cy + sy, cz - sz),
        (cx - sx, cy + sy, cz - sz),
        (cx - sx, cy - sy, cz + sz),
        (cx + sx, cy - sy, cz + sz),
        (cx + sx, cy + sy, cz + sz),
        (cx - sx, cy + sy, cz + sz),
    ]
    faces = [
        (1, 2, 3, 4),
        (5, 8, 7, 6),
        (1, 5, 6, 2),
        (2, 6, 7, 3),
        (3, 7, 8, 4),
        (4, 8, 5, 1),
    ]
    return Mesh(vertices, faces)


def cylinder(center: Vec3, radius: float, height: float, segments: int = 16, axis: str = "z") -> Mesh:
    cx, cy, cz = center
    vertices: list[Vec3] = []
    for z_offset in (-height / 2, height / 2):
        for idx in range(segments):
            angle = 2 * math.pi * idx / segments
            x = radius * math.cos(angle)
            y = radius * math.sin(angle)
            if axis == "z":
                vertices.append((cx + x, cy + y, cz + z_offset))
            elif axis == "x":
                vertices.append((cx + z_offset, cy + x, cz + y))
            elif axis == "y":
                vertices.append((cx + x, cy + z_offset, cz + y))
            else:
                raise ValueError(f"Unsupported cylinder axis: {axis}")

    faces: list[Face] = []
    bottom = tuple(range(segments, 0, -1))
    top = tuple(range(segments + 1, 2 * segments + 1))
    faces.append(bottom)
    faces.append(top)
    for idx in range(segments):
        a = idx + 1
        b = ((idx + 1) % segments) + 1
        c = b + segments
        d = a + segments
        faces.append((a, b, c, d))
    return Mesh(vertices, faces)


def write_obj(path: Path, mesh: Mesh, name: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        f"# Sinclair synthetic radar RCS asset: {name}",
        "# Units: meters",
        "# Generated by scripts/generate_radar_rcs_assets.py",
        f"o {name}",
    ]
    lines.extend(f"v {x:.6f} {y:.6f} {z:.6f}" for x, y, z in mesh.vertices)
    lines.extend("f " + " ".join(str(index) for index in face) for face in mesh.faces)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_json(path: Path, payload: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def make_sedan() -> Mesh:
    mesh = Mesh([], [])
    mesh.extend(cuboid((0.0, 0.0, 0.55), (4.6, 1.85, 0.75)))
    mesh.extend(cuboid((-0.35, 0.0, 1.12), (2.35, 1.55, 0.72)))
    mesh.extend(cuboid((1.95, 0.0, 0.52), (0.45, 1.78, 0.42)))
    mesh.extend(cuboid((-2.05, 0.0, 0.48), (0.38, 1.78, 0.38)))
    for x in (-1.55, 1.55):
        for y in (-0.98, 0.98):
            mesh.extend(cylinder((x, y, 0.32), 0.34, 0.18, segments=18, axis="y"))
    return mesh


def make_pedestrian() -> Mesh:
    mesh = Mesh([], [])
    mesh.extend(cylinder((0.0, 0.0, 0.95), 0.22, 1.15, segments=18, axis="z"))
    mesh.extend(cylinder((0.0, 0.0, 1.62), 0.16, 0.24, segments=18, axis="z"))
    mesh.extend(cylinder((-0.11, 0.0, 0.32), 0.07, 0.64, segments=12, axis="z"))
    mesh.extend(cylinder((0.11, 0.0, 0.32), 0.07, 0.64, segments=12, axis="z"))
    mesh.extend(cylinder((-0.32, 0.0, 0.93), 0.055, 0.82, segments=12, axis="z"))
    mesh.extend(cylinder((0.32, 0.0, 0.93), 0.055, 0.82, segments=12, axis="z"))
    return mesh


def make_cyclist() -> Mesh:
    mesh = Mesh([], [])
    # Rider.
    mesh.extend(cylinder((0.0, 0.0, 1.16), 0.19, 0.92, segments=16, axis="z"))
    mesh.extend(cylinder((0.0, 0.0, 1.78), 0.14, 0.22, segments=16, axis="z"))
    # Bicycle wheels and frame approximations.
    for x in (-0.72, 0.72):
        mesh.extend(cylinder((x, 0.0, 0.42), 0.36, 0.055, segments=24, axis="y"))
    mesh.extend(cuboid((0.0, 0.0, 0.77), (1.55, 0.055, 0.08)))
    mesh.extend(cuboid((-0.32, 0.0, 0.88), (0.08, 0.055, 0.68)))
    mesh.extend(cuboid((0.5, 0.0, 0.92), (0.08, 0.055, 0.58)))
    mesh.extend(cuboid((0.73, 0.0, 1.06), (0.42, 0.055, 0.08)))
    return mesh


def make_guardrail() -> Mesh:
    mesh = Mesh([], [])
    mesh.extend(cuboid((0.0, 0.0, 0.78), (12.0, 0.18, 0.38)))
    for x in (-5.2, -2.6, 0.0, 2.6, 5.2):
        mesh.extend(cuboid((x, 0.0, 0.36), (0.16, 0.16, 0.72)))
    return mesh


def make_plate() -> Mesh:
    return cuboid((0.0, 0.0, 0.0), (1.0, 0.025, 1.0))


