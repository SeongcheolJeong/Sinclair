"""Generate synthetic canonical radar RCS mesh assets for Sinclair.

The assets are intentionally simple, project-generated geometry. They are not
measured RCS assets and not high-fidelity CAD. Their purpose is to provide a
clean first asset-library layer for RadarSim `sim_rcs` experiments.
"""

